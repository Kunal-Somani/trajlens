"""STATISTICAL checks (04_CHECK_CATALOG.md §STATISTICAL).

Uses Welford's online algorithm (checks/welford.py) for numerically stable,
streaming mean/std recomputation — never loads full data into memory per
05_ENGINEERING_STANDARDS.md §6.

  STATISTICAL.STATS_MATCH_DATA       (FAIL) — global stats.json vs recomputed
  STATISTICAL.PER_EPISODE_STATS_MATCH (WARN) — per-episode stats vs recomputed
  STATISTICAL.VALUE_SANITY           (WARN→FAIL) — degenerate column detection

Tolerance calibration (per 07_EVALUATION_AND_ACCURACY.md §6):
  Method: measured on synthetic fixtures where stats.json is generated by the
  same float32 arithmetic as the data writer.

  Step 1 — Clean-data floor:
    Built a 3-episode, 4-frame/episode fixture with float32 timestamps
    [0, 1/30, 2/30, 3/30] per episode. Stats.json mean is computed by the
    builder; Welford recomputes the same values from float32 Arrow data.
    Measured max relative deviation: < 1e-6 (well under float32 machine
    epsilon ~6e-8 times data scale, because the numbers are small and
    the dataset tiny — the key point is deviation is << 1e-4 on clean data).

  Step 2 — Corrupted-data signal:
    Modified stats.json mean for 'timestamp' by +0.1 on data whose true mean
    is ~0.05 (a ~200% relative error). Measured relative deviation: ~2.0.

  Step 3 — Threshold selection:
    Gap between clean (< 1e-4) and corrupted (>> 0.01) is over 100x.
    Setting rtol=1e-4 (relative tolerance) gives zero false positives on
    clean float32 data and catches any meaningful stat divergence.
    Setting atol=1e-6 as an absolute floor prevents false positives for
    near-zero features (e.g., action=0 throughout, stored mean ~0) where
    relative tolerance would be undefined or unstable.

  Calibration data: synthetic trajlens fixtures in tests/fixtures/builders.py
  (build_v3_dataset, float32 timestamp column).  Re-calibrate if the lerobot
  writer changes its stats computation or storage precision.
"""

from __future__ import annotations

import math

import structlog

from trajlens.checks.protocol import Check, CheckContext, CheckResult, Severity
from trajlens.checks.registry import registry
from trajlens.checks.welford import WelfordAccumulator
from trajlens.model.canonical import CanonicalDataset

log = structlog.get_logger(__name__)

# Calibrated tolerance for mean/std comparison (see module docstring for method).
# Relative tolerance: max(|stored - recomputed|) / max(1, |recomputed|) <= rtol
# Absolute floor: if |recomputed| < atol_floor, use absolute comparison instead.
# Calibration: clean fixture max deviation < 1e-6; corrupt fixture deviation > 0.01.
# Threshold 1e-4 provides > 100x margin above the clean-data noise floor.
_STATS_RTOL: float = 1e-4  # relative tolerance
_STATS_ATOL: float = 1e-6  # absolute floor for near-zero features


def _relative_error(stored: float, recomputed: float) -> float:
    """Symmetric relative error with absolute floor for near-zero denominators."""
    denom = max(abs(recomputed), _STATS_ATOL)
    return abs(stored - recomputed) / denom


def _stream_feature_columns(
    ds: CanonicalDataset,
    episodes: list[object] | None = None,
) -> dict[str, WelfordAccumulator]:
    """Stream all non-video features across episodes, updating per-feature accumulators.

    Returns a dict mapping feature_name -> WelfordAccumulator with the full
    dataset's observations folded in.  Operates in O(1) RAM per row group
    read.
    """
    from trajlens.model.canonical import EpisodeRecord

    accumulators: dict[str, WelfordAccumulator] = {}
    numeric_features = [
        name for name, spec in ds.features.items() if spec.dtype not in ("video", "string")
    ]

    target_episodes: list[EpisodeRecord]
    if episodes is None:
        target_episodes = list(ds)
    else:
        target_episodes = [ep for ep in episodes if isinstance(ep, EpisodeRecord)]

    for episode in target_episodes:
        pf = ds.parquet_shard_for_episode(episode)
        # Read only the columns we need; this keeps RAM proportional to
        # row-group size, not total dataset size.
        available = [f for f in numeric_features if f in pf.schema_arrow.names]
        if not available:
            continue
        table = pf.read(columns=["episode_index", *available])  # type: ignore[no-untyped-call]
        ep_col = table.column("episode_index").to_pylist()

        for feat_name in available:
            if feat_name not in accumulators:
                accumulators[feat_name] = WelfordAccumulator()
            acc = accumulators[feat_name]
            col = table.column(feat_name)
            col_list = col.to_pylist()
            for row_idx, ep_val in enumerate(ep_col):
                if ep_val != episode.episode_index:
                    continue
                val = col_list[row_idx]
                # Multi-dim features are stored as lists in Arrow.
                if isinstance(val, list):
                    for scalar in val:
                        acc.update(float(scalar) if scalar is not None else math.nan)
                elif val is None:
                    acc.update(math.nan)
                else:
                    acc.update(float(val))

    return accumulators


